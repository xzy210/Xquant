def is_trading_day(d: Union[date, datetime, str, None] = None) -> bool:
    """
    判断给定日期是否为A股交易日。

    Args:
        d: date / datetime / "YYYY-MM-DD" 字符串，None 表示今天

    Returns:
        True  = 交易日
        False = 非交易日（周末 / 节假日）
    """
    d = _to_date(d)
    ds = d.isoformat()

    # 调休补班优先级最高：原本是周末但需要交易
    if ds in _EXTRA_TRADING_DAYS:
        return True

    # 周末
    if d.weekday() >= 5:
        return False

    # 法定节假日
    if ds in _HOLIDAYS:
        return False

    return True


def get_non_trading_reason(d: Union[date, datetime, str, None] = None) -> str:
    """
    返回非交易日的原因描述；若是交易日则返回空字符串。

    Args:
        d: 同 is_trading_day

    Returns:
        "" 表示交易日，非空字符串描述休市原因
    """
    d = _to_date(d)
    ds = d.isoformat()

    if ds in _EXTRA_TRADING_DAYS:
        return ""

    weekday_names = ["周一", "周二", "周三", "周四", "周五", "周六", "周日"]
    if d.weekday() == 5:
        return "周六休市"
    if d.weekday() == 6:
        return "周日休市"

    if ds in _HOLIDAYS:
        return f"节假日休市（{ds}）"

    # 未收录年份：仅做周末检查，不阻止交易
    year = d.year
    if year not in (2024, 2025, 2026):
        return ""

    return ""


def next_trading_day(d: Union[date, datetime, str, None] = None) -> date:
    """返回 d 之后（含当天）的下一个交易日"""
    from datetime import timedelta
    cur = _to_date(d)
    for _ in range(30):   # 最多查30天
        if is_trading_day(cur):
            return cur
        cur += timedelta(days=1)
    return cur


# ---------------------------------------------------------------------------
# 内部工具
# ---------------------------------------------------------------------------

def _to_date(d: Union[date, datetime, str, None]) -> date:
    if d is None:
        return date.today()
    if isinstance(d, datetime):
        return d.date()
    if isinstance(d, str):
        return date.fromisoformat(d)
    return d
